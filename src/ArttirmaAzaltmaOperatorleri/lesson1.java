package ArttirmaAzaltmaOperatorleri;

public class lesson1 {

    public static void main(String[] args) {

        int number1 = 15;

        number1++;
        System.out.println(number1);
        ++number1;
        System.out.println(number1);
        number1--;
        System.out.println(number1);
        --number1;
        System.out.println(number1);

    }
}
